public class StringTableObject {
public String str;
public int scope;
public Boolean declared;
	
	
	public StringTableObject() {
		str="";
		scope=0;
		declared=true;
	}
	public StringTableObject(String str,int scope,Boolean declared) {
		this.str=str;
		this.scope=scope;
		this.declared=declared;
	}

	
	
	
	
	
}
